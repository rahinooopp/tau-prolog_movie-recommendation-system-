rating(Movie, Value) :-
    film(Movie, _, _, _, _, _, _, _, Value, _).
high_rating(Movie) :-
    rating(Movie, Value),
    Value >= 8.0.
released_after(Movie, YearLimit) :-
    film(Movie, _, Year, _, _, _, _, _, _, _),
    Year > YearLimit.

short_movie(Movie) :-
    film(Movie, _, _, _, _, Duration, _, _, _, _),
    Duration < 100.

long_movie(Title, MinDuration) :-
    film(Title, _, _, _, _, Duration, _, _, _, _),
    Duration > MinDuration.

% 2. classic_film.
classic_film(Title, MaxYear) :-
    film(Title, _, Year, _, _, _, _, _, _, _),
    Year =< MaxYear.

% 3. top_film

top_film(MinRating, Title) :-
    film(Title, _, _, _, _, _, _, _, Rating, _),
    Rating >= MinRating.

% 4. director_movies.
director_movies(Director, List) :-
    findall(Title, film(Title, Director, _, _, _, _, _, _, _, _), List).

% 5. female_lead_movies
female_lead_movies(List) :-
    findall(Title, (film(Title, _, _, _, _, _, _, Lead, _, _), actress(Lead)), List).

% 6. popular_movies
popular_movies(MinVotes, List) :-
    findall(Title, (film(Title, _, _, _, _, _, _, _, _, Votes), Votes >= MinVotes), List).

% 7. total_duration
total_duration([], 0).
total_duration([Title|Tail], Total) :-
    film(Title, _, _, _, _, Duration, _, _, _, _),
    total_duration(Tail, RestTotal),
    Total is Duration + RestTotal.

%countmovie
count_movies([], 0).
count_movies([_|Tail], Count) :-
    count_movies(Tail, TailCount),
    Count is TailCount + 1.
%find popular movies.
popular_movie(Movie) :-
    film(Movie, _, _, _, _, _, _, _, _, Votes),
